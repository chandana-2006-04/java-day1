public class Operators{
     public static void main(String[] args){
        int a=56;
        int b=46;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a&b);
        System.out.println(a*b);
        System.out.println(a^b);
    }
}